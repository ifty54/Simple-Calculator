#Simple Calculator

a = int(input("put first number= "))
b = int(input("put second number= "))
c = input("enter the operator: ")

if c == "+":
  print("result for a + b is", a + b)

if c == "-":
  print("result for a - b is", a - b)

if c == "*":
  print("result for a * b is", a * b)

if c == "/":
  print("result for a / b is", a / b)

if c == "%":
  print("result for a % b is", a % b)
